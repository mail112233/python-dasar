# kelvin ke fahrenhiet
print("\nPROGRAM KONVERSI KELVIN KE FAHRENHIET\n")

kelvin = float(input('masukin kelvin disini :'))
print("suhu adalah",kelvin,"kelvin")

fahrenhiet = ((9/5) * kelvin) + 32
