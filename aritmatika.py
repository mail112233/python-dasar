# operasi aritmatika

a = 19
b = 3

# operasi tambahan +
hasil = a + b
print(a,'+',b,'=',hasil)

# operasi pengurangan -
hasil = a - b
print(a,'-',b,'=',hasil)

# operasi pengkalian * 
hasil = a * b
print(a,'*',b,'=',hasil)

# operasi pembagian /
hasil = a / b
print(a,'/',b,'=',hasil)

# operasi eksponen (pangkat) **
hasil = a ** b
print(a,'**',b,'=',hasil)

# operasi modulus %
hasil = a % b
print(a,'%',b,'=',hasil)


# operasi // floor divison
hasil = a // b
print(a,'//',b,'=',hasil)

# prioritas operasi,operational precedence

'''
    1. ()
    2. exponen **
    3. perkalian dan teman teman * / ** //
    4. pertambahan dan pengurangan + -s
'''